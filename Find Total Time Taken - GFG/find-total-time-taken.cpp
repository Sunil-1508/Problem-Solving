class Solution {
  public:
    int totalTime(int n, vector<int> &a, vector<int> &ti) {
        // code here
         int t=0;
         map<int,int>m;
         m[a[0]]++;
        for(int i=1;i<n;i++){
            if(m[a[i]]==0){ t+=1; m[a[i]]++;}
            else{ t+=ti[a[i]-1]; }            
        }
        return t;
    }
};


